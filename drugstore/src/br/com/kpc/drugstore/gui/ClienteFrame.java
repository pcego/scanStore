    private void btConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConfirmarActionPerformed
        if (!validarCPF()) {
            return;
        }
        boolean retorno;
        switch (opMenu) {
            case "U":
                retorno = update();
                if (retorno) {
                    JOptionPane.showMessageDialog(this, "Atualizado");
                    btTypeDefault();

                }
                break;
            case "I":
                retorno = insert();
                if (retorno) {
                    JOptionPane.showMessageDialog(this, "Gravado");
                    btTypeDefault();
                    limparCampos();
                }
                break;
            case "D":
                String[] opcoes = {"Sim", "Nao"};
                int opcao = JOptionPane.showOptionDialog(this, "Deseja excluir cliente?",
                        "Confirmar Exclusão", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, opcoes, opcoes[1]);
                //Verificando a Resposta do usuario, se resposta = NÂO, sai da função.
                if (opcao == 1) {
                    return;
                }

                retorno = delete();
                if (retorno) {
                    JOptionPane.showMessageDialog(this, "Excluido");
                    btTypeDefault();
                }
                break;
        }
        opMenu = null;
        btTypeDefault();
    }//GEN-LAST:event_btConfirmarActionPerformed

